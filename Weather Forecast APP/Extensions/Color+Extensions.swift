import UIKit
import Foundation

extension UIColor {
    
    static let SunColor = UIColor(named: "sunColor")
    static let RainColor = UIColor(named: "rainColor")
    static let CloudColor = UIColor(named: "cloudColor")
    static let NightColor = UIColor(named: "nightColor")
    
    static let SVColor = UIColor(named: "SVColor")
    static let FontColor = UIColor(named: "fontColor")
    
    static let locButtonColor = UIColor(named: "locButtonColor")
    
    static let fadedGray = UIColor.gray.withAlphaComponent(0.3)
    static let fadedWhite = UIColor.white.withAlphaComponent(0.3)
    
}
